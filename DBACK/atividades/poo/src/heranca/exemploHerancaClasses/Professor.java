package heranca.exemploHerancaClasses;

public class Professor extends Pessoa{

    private int numeroDeAulas;
    private int qtdTrumas;
    private String turmas;

    public int getNumeroDeAulas() {
        return numeroDeAulas;
    }

    public void setNumeroDeAulas(int numeroDeAulas) {
        this.numeroDeAulas = numeroDeAulas;
    }

    public int getQtdTrumas() {
        return qtdTrumas;
    }

    public void setQtdTrumas(int qtdTrumas) {
        this.qtdTrumas = qtdTrumas;
    }

    public String getTurmas() {
        return turmas;
    }

    public void setTurmas(String turmas) {
        this.turmas = turmas;
    }

    
    
    
}
